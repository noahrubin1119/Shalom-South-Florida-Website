    <h1>The Top 10 Songs of Rabbi Shlomo Carlebach</h1>
	</br>
	<p>By Roni</br>Date</p>
	
		<p>Asking someone to rate the songs of the Father of Modern Jewish Music, Shlomo Carlebach, is like
asking a parent of multiple children to pick his or her favorite child. You’ll never get it right, and
someone will always present reasons why a certain song does or doesn’t belong on the list. Despite the
daunting task, here is a list (in no particular order) of the most popular/influential/ubiquitous songs
from the over 600 compositions appearing on nearly 50 recordings of the late, great Reb Shlomo –</p>

<ul>
	<li>VeHa’er Einenu – Released in 1969 as an entry to the Chassidic Song Festival in Israel, this song
won third place and quickly became a favorite and standard at weddings and bar mitzvahs
during the 70s, and continues to be sung worldwide today.</li>
	<li>Od Yishama – There isn’t a Jewish wedding in which Reb Shlomo’s Od Yishama isn’t heard. Over
the years, nearly every Chassidic singer and band has an adaption of these lyrics, but the
Carlebach version remains the golden standard.</li>
	<li>Od Avinu Chai - This song, released in 1967, was widely recognized as the anthem of the Soviet
Jewry movement and continues to be sung at rallies and gatherings of Jews worldwide.</li>
	<li>Lema’an Achai veRe’ai – In these turbulent times, this classic calls out to us to come together as
brothers and friends for the sake of peace.</li>
	<li>Yisrael Betach BaShem – For years, this song was heard by hundreds of thousands of radio
listeners in Israel as it was played on radio each morning. It sends us the powerful message to
place our trust in Hashem.</li>
	<li>Shifchi KaMayim – A soul-stirring composition that used to bring the “singing and dancing rabbi”
to tears in many of his performances, Shifchi can still be heard at Kumzitzes worldwide.</li>
	<li>LeShana Haba’a B’Yerushalayim – Thousands upon thousands of Jewish wedding dance sets and
Simchat Torah hakafot playlists have concluded with this classic prayer and hope to celebrate
next year in Yerushalayim. Its staccato rhythm is unmistakably Carlebach.</li>
	<li>Mizmor LeDavid &amp; Veshamru – Try to find a shul in the world where one of these compositions
is not used on most Friday night. With the proliferation of “Carlebach Minyanim,” these two
tunes continue to reach the next generation of shul-goers.</li>
	<li>Niggun Krakow and Niggun Neshomele – One of Shlomo Carlebach’s greatest gifts and legacies
are his catchy and meaningful nigunnim. There are times when no lyrics were needed to convey
the emotions and feelings, and he captured this with dozens of niggunim that have stood the
test of time. These two are, perhaps, the most famous.</li>
	<li>Esah Einai – This catchy tune dates back to 1959 and appeared on Reb Shlomo’s first record.
Whether adapted to Anim Zemirot or sung with its original lyrics, this song still sounds
contemporary and fresh and exemplifies the simplicity of his arrangements and compositions.</li>
</ul>

<p>Other notables: Atah Takum, Yachad Yachad, Siman Tov U’Mazal Tov, Yibaneh, U’Vau HaOvdim, Anah
Hashem, Gam Ki Elech</p>
    	
