    <div class="title">THE FORMAT</div>
	<p><span class="title"><center><b>“TODAY’S JEWISH MUSIC…WITH A TASTE OF THE PAST&#8480&quot;</b></center></p>
    <p>&quot;Shalom South Florida&quot; is dedicated to providing its listeners with the highest quality
Jewish radio show. We are committed to featuring the latest hits by the current, most
popular Jewish music artists, while staying true to the roots of Jewish music by
providing the classics. The program features Chassidic artists like YAAKOV SHWEKEY,
MORDECHAI SHAPIRO, AVRAHAM FRIED, SIX13 and SHALSHELES, and Israeli
singers SARIT HADAD, HAREL SKA&#39;AT, CHAIM MOSHE, YEHORAM GAON,
KAVERET, and EYAL GOLAN. For the kids there is always some UNCLE MOISHY,
COUNTRY YOSSI and SHLOCK ROCK. Comedy bits are staples on the show. Yiddish
classics and Klezmer tunes are featured by request.</p>


<div class="title">OTHER FEATURES  - PRIZES &amp; JEWISH NEWS</div>
    <p>Listeners can win sports and concert tickets, show tickets, gift certificates, and other
great surprises. The latest news affecting the Jewish community, including breaking
news and otherwise overlooked stories are shared with the audience. Events taking

place in the South Florida Jewish community are announced (see <a href = "./contact.php">Contact Us</a> to have
your announcement included).</p>
  

    <div class="title">THE STATION</br><img src="images/olidies.webp" height="100px"></div>
    <p>TrueOldies is South Florida’s premier oldies channel and has listeners locally and nationally through its website - <a href="https://player.listenlive.co/63891">http://www.oldiessouthflorida.com/</a>. 
The location and power of their multiple frequencies enable Shalom South Florida to reach listeners throughout the entire South Florida Jewish marketplace in Miami-Dade, Broward, and Palm Beach Counties. 
The major cities in the coverage area include: Hallandale, Hollywood, Sunrise, Coral Springs, Deerfield Beach, Boca Raton, Delray Beach, Boynton Beach, Wellington and West Palm Beach. Check out our <a href="/coverage.php">coverage maps</a> to see the incredible market penetration of Shalom South Florida!!
</p>

    <div class="title">THE HOST - <i>Roni</i></div>
<p>Jewish radio show host, Roni, has been hosting Jewish radio in South Florida since 1986. His goal is to make sure that all listeners have a great time listening to Shalom South Florida every Sunday morning. Roni knows his music - He writes Jewish music columns for various publications, has one of the largest Jewish music collections in the country, and has an M.S. in Journalism & Mass Communication.</p>
<div class="title">THE LISTENERS (DEMOGRAPHICS)<i></i></div>
<p>The show is popular with young and old alike. The unique format of the program attracts
male and female listeners of all ages and Jewish affiliations. There are features for
everyone, ranging from 8 to 88. The income level of the “Shalom South Florida” listener
is above average and the listener profile indicates a very active and outgoing
community member.</p>
<div class="title">THE SOUTH FLORIDA MARKET<i></i></div>
<p>South Florida boasts a Jewish population of nearly 600,000, making it one of the top
three Jewish markets in the country. Approximately 17 percent of the South Florida
population is Jewish. The concentration of this market can be found between the cities
of Miami (to the south) and Boynton Beach (to the north). The South Florida market is
experiencing a continuous expansion, and it boasts one of the fastest growing Jewish
communities in the country.<br />
</p>
